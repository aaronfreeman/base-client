import { useState } from "react";

export type Icons = keyof typeof IconSet;

interface Props {
  name: Icons;
  onClick?: () => any;
}

const Icon = ({ name, onClick }: Props) => {
  const [running, setRunning] = useState(false);
  const icon = IconSet[name];

  if (!icon) throw "Could not find icon with name: " + name;

  const click = async () => {
    try {
      setRunning(true);
      await onClick();
    } catch (e) {
      throw e;
    } finally {
      setRunning(false);
    }
  };

  if (onClick) {
    return (
      <span onClick={running ? null : click} className="cursor-pointer">
        {running ? IconSet.Spinner : icon}
      </span>
    );
  }

  return icon;
};

